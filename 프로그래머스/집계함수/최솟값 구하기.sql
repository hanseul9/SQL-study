-- Lv2 MySQL, Oracle
SELECT 
    DATETIME
FROM
    ANIMAL_INS
WHERE 
    DATETIME = (SELECT MIN(DATETIME) FROM ANIMAL_INS);


-- order by + limit 으로도 가능 

-- MYSQL
SELECT 
    DATETIME
FROM 
    ANIMAL_INS 
ORDER BY 
    DATETIME ASC
LIMIT 1;

-- Oracle
SELECT 
    DATETIME
FROM 
    ANIMAL_INS 
ORDER BY 
    DATETIME ASC
FETCH FIRST 1 ROWS ONLY;