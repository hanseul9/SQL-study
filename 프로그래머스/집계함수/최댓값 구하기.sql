-- Lv1 MySQL, Oracle
SELECT 
    DATETIME
FROM
    ANIMAL_INS
WHERE 
    DATETIME = (SELECT MAX(DATETIME) FROM ANIMAL_INS);


-- order by + limit 으로도 가능 

-- MYSQL
SELECT 
    DATETIME
FROM 
    ANIMAL_INS 
ORDER BY 
    DATETIME DESC
LIMIT 1;

-- Oracle
SELECT 
    DATETIME
FROM 
    ANIMAL_INS 
ORDER BY 
    DATETIME DESC
FETCH FIRST 1 ROWS ONLY;